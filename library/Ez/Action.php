<?php
class Ez_Action extends Zend_Controller_Action
{
	public function setSectionId($id)
	{
		$this->view->sectionId = $id;
	}

	public function setSectionTitle($title)
	{
		
		$this->view->sectionTitle = $title;
	}
	
	public function setPageTitle($title)
	{
		$this->view->pageTitle = $title;
	}
}